n = 31
sum = 0
ans = set()
for i in range(0, n + 1):
    for j in range(0, n + 1):
        for k in range(0, n + 1):
            for l in range(0, n + 1):
                if i ^ j ^ k ^ l == n:
                    a = [i, j, k, l]
                    a.sort()
                    if i + j + k + l > sum:
                        sum = i + j + k + l
                        ans = set()
                        ans.add(tuple(a))
                    elif i + j + k + l == sum:
                        ans.add(tuple(a))
                        
print(sum)
for x in ans:
    print(x)

